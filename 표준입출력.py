print(38)  # 정수형
print("문자열")  # 문자열
print(1 + 3)
print("파" + "이" + "썬")
print("파""이""썬")
print("파", "이", "썬")  # 콤마 : 구분자로 한칸의 공백을 가진다.
print("파\n\n\n이\t\t썬")  # \n : 줄바꿈, \t : 한칸 띄우기

print([1, 2, 3])  # 리스트
print({1, 2, 3})  # 세트 (중복된 원소를 허용X)
print((1, 2, 3))  # 튜플 (변경 불가능한 데이터 타입)
print({"key": "value"})  # 딕셔너리 (키와 값의 쌍을 포함)
